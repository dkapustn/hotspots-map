import { toast } from "sonner";

/**
 * Показывает гостю (анонимному входу) предложение зарегистрироваться.
 * Вызывается из обработчиков действий, недоступных гостям
 * (лайк, визит, комментарий, оценка, закладка, создание метки).
 */
export function notifyGuest() {
  toast.info("Войдите в аккаунт", {
    description: "Гости могут только просматривать. Зарегистрируйтесь, чтобы взаимодействовать.",
  });
}
